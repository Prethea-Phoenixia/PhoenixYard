class KdCurve(Enum):
    def __init__(self, desc, mach, drag, ffcr=None):
        self.desc = desc
        self.mach = mach
        self.drag = drag
        self.ffcr = ffcr

    def Kd(self, M):
        """
        linear interpolation between two closest points
        """
        mach = self.mach
        drag = self.drag
        n = len(mach)
        if M < mach[0] or M > mach[n - 1]:
            raise ValueError(
                "Mach value {:} is out of range for {:}-{:}".format(
                    M, mach[0], mach[n - 1]
                )
            )

        """
        Use the Python built in library (optimized C code) to find an insertion
        point such that:
        all(val < M for val in mach[:j]) on the LHS
        and:
        all(val >= M for val in mach[j:]) on the RHS
        """
        j = bisect(mach, M, hi=n - 1)
        i = j - 1

        Mi, Mj = mach[i], mach[j]
        BCi, BCj = drag[i], drag[j]

        k = (M - Mi) / (Mj - Mi)

        return k * BCj + (1 - k) * BCi

    def ff(self, crh):
        """
        Given the parameters about the shape of the shell, correct for the
        effective form factor.
        """
        ffcr = self.ffcr
        if ffcr is None:
            raise ValueError("No Form Factor Scaling Available for this Type.")

        j = bisect(crhs, crh, hi=len(crhs) - 1)
        i = j - 1

        crh_i, crh_j = crhs[i], crhs[j]
        ff_i, ff_j = ffcr[i], ffcr[j]
        if any((ff_i == "", ff_j == "")):
            raise ValueError("CRH out of range")

        k = (crh - crh_i) / (crh_j - crh_i)

        return k * ff_j + (1 - k) * ff_i

